package to.msn.wings.lenzeed.Model;

public class Result {
    private String exhibitid;
    private String exhibitname;
    private String exhibitimage;
    private String exhibitdetail;

    public Result() {
    }

    public String getExhibitid() {
        return exhibitid;
    }

    public void setExhibitid(String exhibitid) {
        this.exhibitid = exhibitid;
    }

    public String getExhibitname() {
        return exhibitname;
    }

    public void setExhibitname(String exhibitname) {
        this.exhibitname = exhibitname;
    }

    public String getExhibitimage() {
        return exhibitimage;
    }

    public void setExhibitimage(String exhibitimage) {
        this.exhibitimage = exhibitimage;
    }

    public String getExhibitdetail() {
        return exhibitdetail;
    }

    public void setExhibitdetail(String exhibitdetail) {
        this.exhibitdetail = exhibitdetail;
    }


}
